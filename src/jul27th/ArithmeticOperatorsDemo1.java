package jul27th;

public class ArithmeticOperatorsDemo1 {

	public static void main(String[] args) {
		
		int x=5,y=2;
		
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		
		System.out.println((double)x/y);
		

	}

}
