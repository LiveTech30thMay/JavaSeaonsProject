package jul10th;

public class DataTypesDemo1 {

	public static void main(String[] args) {
		
		byte b=10;
		short s=32767;
		int i=2147483647;
		long x=2147483648L;
		float p=3.14f;
		double d=3.14;
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(x);
		System.out.println(p);
		System.out.println(d);

	}

}
